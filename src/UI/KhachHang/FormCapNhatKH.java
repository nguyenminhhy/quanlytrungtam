    private void btnXacNhanCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanCapNhatActionPerformed
        String TenKH = txtCNTenKH.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        String NgaySinh = "";
        if (dateCapNhatKH.getDate() == null) {
            System.out.println("Nhap ngay thang di cu");
        } else {
            NgaySinh = sdf.format(dateCapNhatKH.getDate());
        }
        int GioiTinh = cbGioiTinhKH.getSelectedIndex();
        String Sdt = txtCNSdt.getText();
        String Diem = txt_CNDiem.getText();
        String DiaChi = txt_CNDiaChi.getText();
        String cbCC = (String) cbChungChi.getSelectedItem();
        int ChungChi;
        if (cbCC.equals("IELTS")) {
            System.out.println("chay vao chung chi");
            ChungChi = 90;
            System.out.println(ChungChi);
        } else if (cbCC.equals("TOEIC LR")) {
            ChungChi = 91;
        } else if (cbCC.equals("CAMBRIDGE")) {
            ChungChi = 92;
        } else {
            ChungChi = 93;
        }
        if (TenKH.equals("") || NgaySinh.equals("") || Sdt.equals("") || Diem.equals("") || DiaChi.equals("")) {
            JOptionPane.showMessageDialog(null, "Bạn cần nhập đủ thông tin");
        } else {
            boolean kt = busKH.capnhatKH(new dto_KhachHang(maKH, TenKH, NgaySinh, GioiTinh, DiaChi, Sdt, Diem, ChungChi));
            if (kt == true) {
                JOptionPane.showMessageDialog(null, "Cập thành công");
                this.dispose();
                khachhangUI.showTable();
            } else {
                JOptionPane.showMessageDialog(null, "WRONG");
            }
        }
    }//GEN-LAST:event_btnXacNhanCapNhatActionPerformed

