    void showDSChungChi() {
        dsChungChi = busKH.layDS_ChungChi();
        if (dsChungChi != null) {
            for (int i = 0; i < dsChungChi.size(); i++) {
                cbChungChi.addItem(dsChungChi.get(i));
            }
        }
    }

